// Helper functions
export function getStudentById(id: string): Student | undefined {
  return MOCK_STUDENTS.find((student) => student.id === id);
}

export function getStudentsByClass(
  classId: string,
  sectionId?: string,
): Student[] {
  return MOCK_STUDENTS.filter((student) => {
    if (sectionId) {
      return student.classId === classId && student.sectionId === sectionId;
    }
    return student.classId === classId;
  });
}

export function searchStudents(query: string): Student[] {
  const lowerQuery = query.toLowerCase();
  return MOCK_STUDENTS.filter(
    (student) =>
      student.firstName.toLowerCase().includes(lowerQuery) ||
      student.lastName.toLowerCase().includes(lowerQuery) ||
      student.rollNumber.toLowerCase().includes(lowerQuery) ||
      student.email?.toLowerCase().includes(lowerQuery) ||
      student.phone?.includes(query),
  );
}

export function getStudentFullName(student: Student): string {
  return `${student.firstName} ${student.lastName}`;
}

export function getStudentInitials(student: Student): string {
  return `${student.firstName[0]}${student.lastName[0]}`.toUpperCase();
}

export function filterStudentsByStatus(status: Student["status"]): Student[] {
  return MOCK_STUDENTS.filter((student) => student.status === status);
}

export function getActiveStudentsCount(): number {
  return MOCK_STUDENTS.filter((student) => student.status === "active").length;
}

export function calculateAge(dateOfBirth: string): number {
  const today = new Date();
  const birthDate = new Date(dateOfBirth);
  let age = today.getFullYear() - birthDate.getFullYear();
  const monthDiff = today.getMonth() - birthDate.getMonth();
  if (
    monthDiff < 0 ||
    (monthDiff === 0 && today.getDate() < birthDate.getDate())
  ) {
    age--;
  }
  return age;
}
